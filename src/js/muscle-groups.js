/**
 * Отримати опис групи м'язів
 * @param {string} muscleName
 * @returns {Object}
 */
export function getMuscleGroupDescription(muscleName) {
  const key = muscleName.toLowerCase();
  
  // Спеціальна обробка для "abdominals" -> "abs"
  if (key === 'abdominals' || key.includes('abs')) {
    return MUSCLE_GROUPS_DESCRIPTIONS.abs || {
      name: 'Вправи для преса',
      description: 'М\'язи преса (абдомінали) - це група м\'язів, які розташовані в передній частині тулуба.',
    };
  }
  
  // Спеціальна обробка для "serratus anterior"
  if (key.includes('serratus') || key.includes('anterior')) {
    return MUSCLE_GROUPS_DESCRIPTIONS['serratus anterior'] || {
      name: 'Вправи для серратних м\'язів',
      description: 'Серратні м\'язи (передні зубчасті м\'язи) розташовані на бічних сторонах грудної клітки та відповідають за рух лопаток та стабілізацію плечового поясу.',
    };
  }
  
  // Спеціальна обробка для "cardiovascular system"
  if (key.includes('cardiovascular') || key.includes('cardio')) {
    return MUSCLE_GROUPS_DESCRIPTIONS['cardiovascular system'] || {
      name: 'Кардіо вправи',
      description: 'Кардіо вправи спрямовані на покращення роботи серцево-судинної системи.',
    };
  }
  
  // Спеціальна обробка для "levator scapulae"
  if (key.includes('levator') || key.includes('scapulae')) {
    return MUSCLE_GROUPS_DESCRIPTIONS['levator scapulae'] || {
      name: 'Вправи для м\'язя, що піднімає лопатку',
      description: 'М\'яз, що піднімає лопатку (леватор лопатки) - це м\'яз верхньої частини спини.',
    };
  }
  
  // Спеціальна обробка для "quads" -> "quadriceps"
  if (key === 'quads' || key.includes('quad')) {
    return MUSCLE_GROUPS_DESCRIPTIONS.quads || MUSCLE_GROUPS_DESCRIPTIONS.quadriceps || {
      name: 'Вправи для квадріцепсів',
      description: 'Чотириголові м\'язи стегна (квадріцепси) - це найбільші м\'язи ніг.',
    };
  }
  
  return MUSCLE_GROUPS_DESCRIPTIONS[key] || {
    name: `Вправи для ${muscleName}`,
    description: `Вправи для розвитку м'язів ${muscleName}.`,
  };
}
